package day01;

/*
    변수의 선언과 저장
*/
public class variable {
    public static void main(String[] args) {
        int x=5;
        System.out.println(x);

        x=10;
        System.out.println(x);

        int y = 5;
        int z = 3;
        System.out.println(y+z);
        System.out.println(y-z);
        System.out.println(y*z);
        System.out.println(y/z);
    }
}
